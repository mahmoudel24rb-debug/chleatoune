// Cartes pixel-art des 16 poissons de Chléatoune — style « Animal Crossing »
// ============================================================================
// Gabarit :
//   • Grille 32×20 pixels pour toutes les espèces, SAUF l'hippocampe : 16×28
//     (vertical, tête en haut).
//   • Poisson vu de profil 3/4, tête à GAUCHE, queue à droite.
//   • 2 frames par espèce : queue HAUTE puis queue BASSE — seuls la queue,
//     les tentacules ou les voiles bougent, le corps ne bouge pas d'un pixel.
//   • 1 caractère = 1 pixel ; '.' = transparent ; chaque autre caractère est
//     une clé de la palette (K = contour foncé teinté — jamais de noir pur —,
//     C = corps, V = ventre clair, O = œil sombre, R = reflet blanc de l'œil,
//     + clés spécifiques à l'espèce : rayures, points, branchies, voiles…).
//   • Le gros œil rond 4×4 (5×5 pour l'anchois) avec son reflet 1 px en haut
//     à gauche est OBLIGATOIRE : c'est lui qui donne la bouille Animal Crossing.
// ============================================================================

